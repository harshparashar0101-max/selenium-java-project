package com.test;

public class loop {

    public class LoopExample {
    public static void main(String[] args) {
        System.out.println("Counting from 1 to 5:");

        // Start at 1; stay in loop while i <= 5; add 1 to i each time
        for (int i = 1; i <= 5; i++) {
            System.out.println("Number: " + i);
        }
    }
}

}
